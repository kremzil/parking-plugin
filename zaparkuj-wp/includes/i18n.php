function zp_get_lang(){
  $allowed = ['sk','en','pl','hu','sv','zh'];
  $q = isset($_GET['lang']) ? strtolower(sanitize_text_field($_GET['lang'])) : '';
  if ($q && in_array($q, $allowed, true)) return $q;
  $locale = function_exists('determine_locale') ? determine_locale() : get_locale();
  $locale = strtolower($locale ?: '');
  if (strpos($locale, 'sk') === 0) return 'sk';
  if (strpos($locale, 'en') === 0) return 'en';
  if (strpos($locale, 'pl') === 0) return 'pl';
  if (strpos($locale, 'hu') === 0) return 'hu';
  if (strpos($locale, 'sv') === 0) return 'sv';
  if (strpos($locale, 'zh') === 0) return 'zh';
  return 'sk';
}

